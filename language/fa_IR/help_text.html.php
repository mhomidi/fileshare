<h3>به سامانه ارسال فایل دانشگاه تهران خوش آمدید</h3>

<p>

    سامانه ارسال فایل دانشگاه تهران یک برنامه تحت وب است که به کاربران احراز شده
    چنین امکانی را می‌دهد تا ساده و امن فایل‌های بزرگ را به کاربران دیگر ارسال کنند.
    این سامانه برای تامین نیازهای جامعه‌ی تحصیل کرده گسترش یافته است.
</p>

<h4>برای کاربران احراز شده</h4>

<p>

    اگر شما رمز عبور و یا نام کاربری خود را از مرکز خود ندارید،
    باید با مسئول انفورماتیک خود تماس بگیرید. شما به عنوان کاربر احراز شده می‌توانید فایل‌ها را
    یک یا چند بار بارگذاری کنید و برای دانلود می‌توانید یک لینک برای گیرندگان آن فایل بفرستید.

</p>

<h3>امکان دانلود با هر حجمی</h3>
<p>

    تمام مرورگرهای جدید می‌توانند هر فایلی را با هر حجمی دانلود کنند
</p>

<h3>Pامکان بارکذاری با هر حجمی</h3>

<p>
    اگر مرورگر شما از HTML5 پشتبانی می‌کند، شما می‌توانید هر فایلی را حداکثر به میزان {size:cfg:max_transfer_size} بارگذاری کنید.
</p>

<h3>ویژگی‌های مرورگر شما</h3>
<ul class="fa-ul">
    <li data-feature="html5"><img src="images/html5_installed.png" alt="HTML5 upload enabled" /> شما می‌توانید هر فایلی‌ را با سایز کوچکتر از {size:cfg:max_transfer_size} در هر انتقال بارگذاری کنید.</li>
    <li data-feature="nohtml5"><img src="images/html5_none.png" alt="HTML5 upload disabled" /> شما می‌توانید فایل‌ها را که حداکثر {size:cfg:max_legacy_file_size} و در مجموع {size:cfg:max_transfer_size} را در هر انتقال استفاده کنید.</li>
</ul>

