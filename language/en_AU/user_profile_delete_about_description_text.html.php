    <h2>Actions</h2>

    <p>
        Your user name and password information is handled outside of
        FileSender of UT itself. You are free to delete your account on this
        FileSender of UT installation which will remove all of your files,
        all the guests that you have invited, and any other debug
        information you might have sent to the server (failed upload,
        etc). Deleting your user account with FileSender of UT removes all
        of this information and FileSender of UT will no longer retain this
        information. Because FileSender of UT does not have authority over
        your user name and password, they will remain active until you
        delete them from your institution.
    </p>
    <p>
        Because the user name and password checking is handled outside
        of FileSender of UT you may be able to return to the system and
        login again. If you do this, you will appear as a new user to
        FileSender of UT and will not be able to recover any of your old
        files or information because that has been completely deleted
        from FileSender of UT when you deleted your FileSender of UT account.
    </p>
